		<div class="lg-flex">
			<div class="flex-item lg-col-5 px3">
				<figure style="text-align: center;">
					<iframe src="//www.slideshare.net/slideshow/embed_code/key/fHHniexPQZVePL" frameborder="0" marginwidth="0" marginheight="0" scrolling="no" style="border: 1px solid #CCC; border-width: 1px; margin-bottom: 5px; max-width: 100%; width: 100%; height: 100%; min-height: 400px;" allowfullscreen> </iframe>
