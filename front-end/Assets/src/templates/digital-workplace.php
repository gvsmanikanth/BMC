			<div class="flex-item lg-col-7">
				<p>
					Catalog sprawl is the newest challenge for data center administrators pursuing IT service management (ITSM) excellence. It’s when employees are forced to search through scattered catalogs to find the resource they need. The impact of catalog sprawl &ndash; the average company has 20 for IT and business services &ndash; is increased labor costs and low customer satisfaction. <a href="/blogs/facts-catalog-sprawl/" target="_blank">View the infographic.</a>
				</p>
				<ul class="orange-tick-bullets">
					<li><strong>Aggregate:</strong> Eliminate catalog sprawl by establishing a single portal where all IT and business resources can be requisitioned or acquired.</li>
					<li><strong>Create personas:</strong> Interact with the various user groups to establish personas – employees who are likely to have similar digital needs based on their behaviors.</li>
					<li><strong>Deploy algorithms:</strong> Once the personas have been established, deploy algorithms that suggest appropriate resources based on peer suggestions.</li>
					<li><strong>Provide a recommendation engine:</strong> IT curating involves more than hardware and software selection.</li>
				</ul>
