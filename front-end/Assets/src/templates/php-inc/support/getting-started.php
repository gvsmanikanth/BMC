				<ul class="accordion mt3 mr3" >
					<li class="accordion-item">
						<span id="faq-1" class="jump-to"></span><a class="accordion-item-anchor" href="#faq-1"><span class="align-middle font-size-0-pt-8-em" >How do I Register and Subscribe to support?</span></a>
						<div class="accordion-item-content">
							<p>
								Visit www.bmc.com/support and click on the &lsquo;Register' link in the grey bar at the top of the screen. Enter your details as required and wait for an email confirming your registration and subscription has been processed. You will receive an email with an activation link which you will have to click in order for your registration to be completed.
							</p>
						</div>
					</li>
					<li class="accordion-item">
						<span id="faq-2" class="jump-to"></span><a class="accordion-item-anchor" href="#faq-2"><span class="align-middle font-size-0-pt-8-em" >I don't know my support ID, how do I get it?</span></a>
						<div class="accordion-item-content">
							<p>
								This information is sent to the person(s) in your company who purchased your BMC&nbsp; products. The Support Contract ID information along with the PIN/Password is located on the Order Confirmation Document and the Order Delivery Document. This information is communicated via email in .pdf format. If you cannot locate the provided documents, please email <a href="mailto:Customer_Care@bmc.com">Customer_Care@bmc.com</a> and include your full company name and the BMC products you are using.
							</p>
						</div>
					</li>
					<li class="accordion-item">
						<span id="faq-4" class="jump-to"></span><a class="accordion-item-anchor" href="#faq-4"><span class="align-middle font-size-0-pt-8-em">How do I log new support Cases with BMC Customer Support?</span></a>
						<div class="accordion-item-content">
							<p>
								Visit www.bmc.com/support and click on the &lsquo;Support Login&rsquo; button. From the Support Central page, click on the link &lsquo;Submit New Case&rsquo; just under &lsquo;Case Management&rsquo;. Insert all necessary information on the case and click Submit. Please see the <a href="http://media.cms.bmc.com/documents/BMC+Support+Central+User+Guide.pdf" target="_blank">Support Central User Guide</a> for further details.
							</p>
						</div>
					</li>
					<li class="accordion-item">
						<span id="faq-5" class="jump-to"></span><a class="accordion-item-anchor" href="#faq-5"><span class="align-middle font-size-0-pt-8-em">How do I download products via the BMC EPD page?</span></a>
						<div class="accordion-item-content">
							<p>
								Visit www.bmc.com/support and click on the &lsquo;Support Login&rsquo; button. From the Support Central page, click on the &lsquo;Product Downloads, Patches and Fixes&rsquo; link and then select &lsquo;Product Downloads (EPD)&rsquo;. Please see the <a href="http://media.cms.bmc.com/documents/BMC+Support+Central+User+Guide.pdf" target="_blank">Support Central User Guide</a> for further details.
							</p>
						</div>
					</li>
