					<div class="section-content">
						<p>
							Digital Service Management (DSM) is a groundbreaking new paradigm for IT Service Management (ITSM), based on a forward-looking and human-centric view of how IT is transforming employee productivity and driving innovation in the era of digital services.
						</p>
						<p>
							Digital businesses lead their industries with increased profitability driven in part by a fundamental improvement in human productivity. These digitally advanced companies employ a "digital service first" strategy, by re-engineering their offerings through apps and applications that are:
						</p>
						<ul>
							<li>
								<strong>Mobile and self-service</strong> – used as a leading design framework, rather than an afterthought
							</li>
							<li>
								<strong>Intuitive and intelligent</strong> – based on how people actually use the technology
							</li>
							<li>
								<strong>Automated and data driven</strong> – using smart algorithms to reduce risk of human error, and analytics to optimize every step of the process in real time
							</li>
							<li>
								<strong>Lean serviced</strong> – Automation lowers the workload on your staff, while improving productivity
							</li>
							<li>
								<strong>Crowd supported</strong> – keeping people as an integral part of the process, but not in the way of the process
							</li>
							<li>
								<strong>High-speed</strong> – radically accelerating times to deploy and update services in hours or days, rather than weeks or months
							</li>
						</ul>
