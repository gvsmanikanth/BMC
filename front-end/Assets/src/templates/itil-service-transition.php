			<section class="accordion-content">
				<h2>What is Service Transition?</h2>
				<p>
					ITIL service transition helps plan and manage the change of state of a  service in its lifecycle.&nbsp;Managing risk for new, changed&nbsp;and retired  services protects the product environment. This helps  the&nbsp;business&nbsp;deliver value to itself and its customers.
				</p>
				<img class="img-fl-right-wd" src="http://media.cms.bmc.com/images/itil-service-transition.png" alt="ITIL Service Transition" />
				<p>
					Curating service knowledge helps all stakeholders make informed, reliable decisions and&nbsp;support challenges with service delivery. Both managing service risk and curating service knowledge are integral to service transition.
				</p>
				<p>
					During service transition, the following organizational elements need support:
				</p>
				<ul>
					<li>
						Service Strategy
					</li>
					<li>
						People
					</li>
					<li>
						Process
					</li>
					<li>
						Technology
					</li>
					<li>
						Suppliers of the service
					</li>
					<li>
						Organizational culture
					</li>
					<li>
						Governance
					</li>
					<li>
						Risk
					</li>
				</ul>
				<p>
					No change is without risk. In fact, change can create extra risk. When transitioning services, focus on communication planning for awareness and compliance. One of the biggest challenges in service transition is changing people&rsquo;s behavior to accommodate a new or&nbsp;different service. People have a psychological need to feel safe and comfortable with changes to them and around them.
				</p>
