				<h2><a name="service"></a>Service Validation and Testing</h2>
				<p>
					Testing can take place at any point in the service lifecycle&nbsp;but, it generally occurs during Service Transition. The service validation and testing process plans, conducts&nbsp;and reports on tests of new or changed services. The results of testing go&nbsp;to the change evaluation process to support a decision on whether to proceed.
				</p>
				<p>
					The service design package (SDP) outlines the tests to perform.&nbsp;
				</p>
				<p>
					Working with change evaluation, service validation and testing will:
				</p>
				<ul>
					<li>
						Work with transition planning and support to plan the      resources required for testing
					</li>
					<li>
						Plan and design tests
					</li>
					<li>
						Schedule tests
					</li>
					<li>
						Prepare the test environment
					</li>
					<li>
						Perform the tests
					</li>
					<li>
						Evaluate exit criteria and report
					</li>
					<li>
						Clean up and close tests
					</li>
					<li>
						&nbsp;
					</li>
				</ul>
				<p>
					Service validation and test will perform different types of tests, as called for in the service design package. Types of tests include:
				</p>
				<ul>
					<li>
						Utility testing.&nbsp;Does the service deliver the      required functionality?
					</li>
					<li>
						Warranty testing.&nbsp;Will the service deliver      required levels of availability, capacity, security, and continuity?
					</li>
					<li>
						Usability testing.&nbsp;Will the service be usable by      all&nbsp;potential users, including those with restricted abilities?
					</li>
					<li>
						Contract and regulation testing.&nbsp;Will the service      conform to applicable regulatory and contract requirements?
					</li>
					<li>
						Operational readiness testing.&nbsp;Are the support functions,      including the service desk,&nbsp;staffed and trained to support the new or      changed service?
					</li>
				</ul>
			</section>

