			<section>
				<h2>What is IT Operations Analytics?</h2>
				<p>
					IT Operations Analytics (ITOA) automates the process of collecting, organizing, and identifying patterns in highly distributed, diverse and fast-changing service and application data to identify problems faster and improve IT system performance.
				</p>
				<img class="layout-right width-xs ml3em" src="http://media.cms.bmc.com/images/it-operations-analytics.png" alt="IT Operations Analytics" />
				<p>
					The modern digital business revolution infuses technology in every step of an organization&rsquo;s value chain. This unprecedented use of technology creates a complex challenge for IT Infrastructure and Operations organizations, as it becomes difficult to collect and organize the explosion of data generated by new digital business systems and sources.
				</p>
				<p>
					Operational analytics gives IT Operations Management (ITOM) teams the right information, at the right time. It allows them to quickly focus their efforts, reduce the time it takes to solve problems, proactively identify issues and optimize system and application performance to support business needs.
				</p>
			</section>

