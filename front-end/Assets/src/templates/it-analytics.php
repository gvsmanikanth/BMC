			<section>
				<h2>Data-driven service optimization</h2>
				<p>
					A single, architected platform for IT Operations Analytics transforms web scale, real-time streaming data, generated by the digital business, into meaningful, actionable insights that fuel fast, business-aligned decisions for IT. To optimize applications and IT-dependent services, the analytics platform must collect and analyze a&nbsp; wide variety of data, including metric, event, log, user experience, IT service management, customer service and sentiment, business and social data, and applying big data analytics in three sequential steps:
				</p>
				<ul>
					<li>
						<b>Data capture and ingestion:</b> real-time streaming, transformation, storage and indexing
					</li>
					<li>
						<b>Analytics and machine learning:</b> automated baselines and abnormality detection, pattern discovery, statistics and probability, recommendation engine
					</li>
					<li>
						<b>Access and explore:</b> visualize, search, correlate and compare, trend, predict and collaborate
					</li>
				</ul>
