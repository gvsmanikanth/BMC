				<h2>
					<a name="supplier"></a>Supplier Management
				</h2>
				<p>Supplier management works with third parties, such as&nbsp;suppliers,&nbsp;to negotiate contracts for products or services.&nbsp;&nbsp;Supplier management&nbsp;monitor conformance to the contract conditions and address any breaches. At renewal, supplier management will determine whether to renew, renegotiate, or end the contract.</p>
				<p>The objectives of supplier management is&nbsp;to ensure alignment of&nbsp;contracts&nbsp;with the needs of the business. It is also responsible for ensuring&nbsp;suppliers are meeting their commitments.&nbsp;The supplier and contract management information system (SCMIS) holds&nbsp;supplier and contract details.&nbsp;</p>
			</section>
			<section>
				<h2>
					<a name="design"></a>Design Coordination
				</h2>
				<p>The central principles in design coordination are balance,&nbsp;prioritization&nbsp;and integration with project management. Balance and prioritization address the utility and warranty of a service, as well as the needs of the service throughout its lifecycle.&nbsp;</p>
				<p>Design coordination oversees all activity in the Service Design phase of the service lifecycle. Its aim is to ensure that a holistic, integrated approach is taken to the design of services. This is necessary because of the variety of disciplines involved in Service Design and the need to take a consistent approach.</p>
				<p>Design coordination is accountable for the production of the service design package (SDP).&nbsp;The SDP&nbsp;is a comprehensive description of how a new or changed service is to be designed, built, tested, deployed, and operated. The SDP is the handoff from the Service Design phase to the Service Transition phase.</p>
				<p>Design coordination handles managing&nbsp;resources needed by the Service Design phase of the lifecycle. This includes:&nbsp;</p>
				<ul>
					<li>planning to ensure that adequate resources are available&nbsp;</li>
					<li>scheduling access to resources among the many projects that may be in this phase at any one time</li>
				</ul>
				<p>It is accountable for the&nbsp;performance and improvement of the Service Design phase of the lifecycle.</p>
