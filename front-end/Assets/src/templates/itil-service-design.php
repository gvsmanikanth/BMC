				<h2>
					<a name="availability"></a>Availability Management
				</h2>
				<p>Availability Management ensures&nbsp;that&nbsp;infrastructure,&nbsp;tools, roles etc. are appropriate for the agreed&nbsp;targets.&nbsp;It also works with the design teams to ensure that availability is designed into services.</p>
				<p>Part of the process is to identify vital business functions (VBFs) which IT services support. This will help clarify which approach to availability to take:&nbsp;</p>
				<ul>
					<li>prevention (making sure, as far as possible, that unavailability never happens)&nbsp;</li>
					<li>recovery (developing plans to restore service rapidly in the event of an outage).</li>
				</ul>
				<p>Availability management views availability from the user&rsquo;s perspective, from end to end. This means identifying single points of failure and designing resilience into any&nbsp;infrastructure supporting the service. Availability management serves as a focal point for all issues in IT Service Management related to availability.&nbsp;</p>
				<p>Availability management handles specifying which metrics to&nbsp;use&nbsp;to measure availability. And,&nbsp;monitors availability to ensure that the SLA targets are met.</p>
			</section>
			<section>
				<h2>
					<a name="continuity"></a>IT Service Continuity Management
				</h2>
				<p>IT service continuity management (ITSCM) focuses on supporting the overall&nbsp;continuity of the business. We define ITSCM as the process responsible for managing risks that could seriously impact IT services.&nbsp;</p>
				<p>Risks so serious&nbsp;they&nbsp;could&nbsp;threaten the very survival of the business.</p>
				<p>This activity is often referred to as disaster recovery (DR).&nbsp;But, the use of the term ITSCM should show that there is a corresponding business continuity management (BCM) process.&nbsp;ITSCM&nbsp;supports the BCM process.</p>
				<p>ITSCM must work closely with BCM to perform risk analysis and&nbsp;business impact analysis (BIA). This analysis determines how different types of disruptions impact the business. The business areas determined to suffer the greatest impact&nbsp;need the most focus from&nbsp;the service continuity teams.</p>
				<p>ITSCM is responsible for development and deployment of the service continuity plan.&nbsp;This includes regular testing and&nbsp;training of all personnel&nbsp;associated with the plan.&nbsp;ITSCM also works with change management to ensure that continuity plans are&nbsp;updated&nbsp;as the operational model changes.</p>
				<p>
					There are four stages in the ITSCM lifecycle process:<br />1. Initiation<br />2. Requirements and strategy<br />3. Implementation<br />4. Ongoing operation
				</p>
				<p>
