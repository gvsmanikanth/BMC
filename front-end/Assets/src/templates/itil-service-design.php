				<h2>
					<a name="catalog"></a>Service Catalog Management
				</h2>
				<p>Service catalog management ensures that an accurate and up-to-date service catalog is&nbsp;available to all parties authorized to see it. All parts of IT Service Management, as well as&nbsp;customers and users, use the service catalog.&nbsp;Accuracy and availability are essential.</p>
				<p>Service catalog management must work closely with service portfolio management as new services move from the pipeline into the catalog and older services are retired. It also helps define how services can be&nbsp;requested&nbsp;and what options are available (gold/silver levels, for instance). The service catalog should document all defined services.&nbsp;</p>
				<p>The service catalog generally comprises two views:&nbsp;</p>
				<ul>
					<li>a business service view that is visible to the customer&nbsp;</li>
					<li>a technical service view that is visible only to IT personnel.&nbsp;</li>
				</ul>
				<p>This enables the customer to choose services based on their business requirements.&nbsp;At the same&nbsp;IT personnel can use their view to determine what technical services they need to support a given business service.&nbsp;</p>
