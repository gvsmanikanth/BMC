				<h2>Service Level Management</h2>
				<a name="level"></a>
				<p>The service level management (SLM) process focuses on researching and understanding requirements.&nbsp;Areas include:</p>
				<ul>
					<li>defining, negotiating, agreeing upon and documenting IT service targets</li>
					<li>monitoring, measuring and reporting on how well the service provider delivered the agreed upon targets&nbsp;</li>
				</ul>
				<p>When targets are appropriate and met, then the business and IT have a better chance of becoming aligned.&nbsp;</p>
				<p>Agreed upon targets are often&nbsp;spelled out in service level agreements (SLAs). Monitoring, measuring and reporting on SLA's in this way provides close links to Continual Service Improvement (CSI).&nbsp;</p>
				<p>SLAs are agreements to provide specific services at a defined level of quality (warranty) for a specific price. SLAs typically need negotiation of&nbsp;agreements with other internal organizations (OLA's) or external&nbsp;suppliers (Underpinning Contracts).&nbsp;</p>
				<p>Negotiating SLAs to ensure service commitments are met, service level management works&nbsp;with the following warranty processes:</p>
				<ul>
					<li>capacity management&nbsp;</li>
					<li>availability management&nbsp;</li>
					<li>security management</li>
					<li>service continuity management</li>
				</ul>
				<p>Service level management is accountable for monitoring conformance to the SLAs and take action if there is a breach of the SLA. This means working with the service desk, incident management, and problem management.&nbsp;</p>
				<p>Customer satisfaction is not determined only by SLA performance.&nbsp;Therefore service level management should meet with customers face-to-face on a regular basis. This helps&nbsp;to maintain a positive relationship address any concerns the customer may have.</p>
